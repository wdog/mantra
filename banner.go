import "fmt"

// boldFg renders text in bold with a 24-bit RGB foreground color.
func boldFg(r, g, b int, text string) string {
	if noColor {
		return text
	}
	return fmt.Sprintf("\033[1;38;2;%d;%d;%dm%s\033[0m", r, g, b, text)
}

